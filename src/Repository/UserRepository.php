    /**
     * @return User[]
     */
    public function getUsersLastHour(): array
    {
        $oneHourAgo = new \DateTime('now', new \DateTimeZone('UTC'));

        return $this->createQueryBuilder('u')
            ->andWhere('u.last_login >= :oneHourAgo')
            ->setParameter('oneHourAgo', $oneHourAgo)
            ->getQuery()
            ->getResult();


    }

